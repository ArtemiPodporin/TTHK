# Создаем список с именами студентов, в котором есть повторяющиеся имена
студенты = ['Juhan','Kati','Mario','Mario','Mati','Mati']
# Создаем пустой список, куда будем добавлять уникальные имена
уникальные_имена = []
# Проходим по всем именам в исходном списке
for студент in студенты:
    # Проверяем, что имя еще не было добавлено в список уникальных имен
    if студент not in уникальные_имена:
        # Если имя не было добавлено, то добавляем его
        уникальные_имена.append(студент)
# Выводим список с уникальными именами
print("уникальные ученики:", уникальные_имена)


